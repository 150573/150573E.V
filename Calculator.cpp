#include<iostream>
using namespace std;
int main()
{
    int num1, num2, add, multiply, divide, subtract;
    cout<<"entre num1";//entre num1
    cin>>num1;
    cout<<"entre num2";//entre num2
    cin>>num2;
    add=num1+num2;//addition of num1 and num2
    cout<<"sum is"<<add<<"\n";//answer of addition
    multiply=num1*num2;//multiplication of num1 and num2
    cout<<"product is"<<multiply<<"\n";//answer of multiplication
    divide=num1/num2;//division of num1 and num2
    cout<<"divide is"<<divide<<"\n";//answer of division
    subtract=num1-num2;// subtraction of num1 and num2
    cout<<"difference is"<<subtract<<"\n";//answer of subtraction
    return 0;
}